// While Loop
// The while loop loops through a block of code as long as a specified condition is true
#include <stdio.h>

int main()
{
    int i = 0;
    int j = 0;
    int k = 1;
    int l = 1;

    while (i, j, k, l < 5)
    {
        printf("%d\n", i);
        i++;
        printf("%d\n", j);
        j++;
        printf("%d\n", k);
        k++;
        printf("%d\n", l);
        l++;
    }

    return 0;
}